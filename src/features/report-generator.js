const crypto = require('crypto');
const { EventEmitter } = require('events');

/**
 * Report configuration
 */
class ReportConfig {
  constructor(config = {}) {
    this.id = crypto.randomBytes(12).toString('hex');
    this.name = config.name;
    this.type = config.type; // 'competitor_activity', 'price_history', 'tech_evolution', 'alert_summary'
    this.dateRange = config.dateRange || {
      start: Date.now() - 7 * 24 * 60 * 60 * 1000, // 7 days default
      end: Date.now()
    };
    this.monitors = config.monitors || []; // Monitor IDs to include
    this.includeCharts = config.includeCharts !== false;
    this.includeTrends = config.includeTrends !== false;
    this.format = config.format || 'html'; // 'html', 'pdf'
    this.timezone = config.timezone || 'UTC';
    this.scheduling = config.scheduling || null; // For scheduled reports
    this.metadata = config.metadata || {};
  }
}

/**
 * Generated report
 */
class Report extends EventEmitter {
  constructor(config) {
    super();

    this.id = crypto.randomBytes(12).toString('hex');
    this.config = config;
    this.generatedAt = Date.now();
    this.status = 'pending'; // pending, generating, completed, failed
    this.content = null;
    this.format = config.format;
    this.size = 0;
    this.error = null;
    this.sections = [];
  }

  /**
   * Add section to report
   */
  addSection(title, content, metadata = {}) {
    this.sections.push({
      id: crypto.randomBytes(8).toString('hex'),
      title,
      content,
      type: metadata.type || 'text',
      createdAt: Date.now(),
      metadata
    });
  }

  /**
   * Generate HTML output
   */
  generateHTML() {
    const title = this.config.name || `Report ${this.id.slice(0, 8)}`;
    const dateStr = new Date(this.generatedAt).toISOString();

    let html = `
<!DOCTYPE html>
<html>
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${title}</title>
  <style>
    body {
      font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, Oxygen, Ubuntu, Cantarell, sans-serif;
      max-width: 1200px;
      margin: 0 auto;
      padding: 20px;
      color: #333;
      line-height: 1.6;
    }
    .header {
      border-bottom: 3px solid #007bff;
      padding-bottom: 20px;
      margin-bottom: 30px;
    }
    .header h1 {
      margin: 0 0 10px 0;
      color: #222;
    }
    .header .meta {
      color: #666;
      font-size: 14px;
    }
    .section {
      margin-bottom: 40px;
    }
    .section h2 {
      color: #007bff;
      border-left: 4px solid #007bff;
      padding-left: 15px;
      margin-bottom: 20px;
    }
    table {
      width: 100%;
      border-collapse: collapse;
      margin: 15px 0;
    }
    table th {
      background: #f5f5f5;
      padding: 12px;
      text-align: left;
      font-weight: 600;
      border-bottom: 2px solid #ddd;
    }
    table td {
      padding: 12px;
      border-bottom: 1px solid #ddd;
    }
    table tr:hover {
      background: #f9f9f9;
    }
    .chart {
      margin: 20px 0;
      padding: 15px;
      background: #f9f9f9;
      border-radius: 8px;
    }
    .metric {
      display: inline-block;
      margin-right: 30px;
      margin-bottom: 15px;
    }
    .metric-value {
      font-size: 24px;
      font-weight: bold;
      color: #007bff;
    }
    .metric-label {
      color: #666;
      font-size: 12px;
      text-transform: uppercase;
      margin-top: 5px;
    }
    .footer {
      margin-top: 50px;
      padding-top: 20px;
      border-top: 1px solid #ddd;
      color: #999;
      font-size: 12px;
    }
  </style>
</head>
<body>
  <div class="header">
    <h1>${title}</h1>
    <div class="meta">
      <p>Generated: ${dateStr}</p>
      <p>Report Type: ${this.config.type}</p>
      <p>Period: ${new Date(this.config.dateRange.start).toLocaleDateString()} - ${new Date(this.config.dateRange.end).toLocaleDateString()}</p>
    </div>
  </div>
`;

    // Add sections
    for (const section of this.sections) {
      html += `
  <div class="section">
    <h2>${section.title}</h2>
    ${section.content}
  </div>
`;
    }

    html += `
  <div class="footer">
    <p>This report was generated by Basset Hound Browser. Report ID: ${this.id}</p>
  </div>
</body>
</html>
`;

    return html;
  }

  /**
   * Get report as plain text
   */
  generateText() {
    let text = `${'='.repeat(60)}\n`;
    text += `${this.config.name || 'Report'}\n`;
    text += `${'='.repeat(60)}\n`;
    text += `\nGenerated: ${new Date(this.generatedAt).toISOString()}\n`;
    text += `Report Type: ${this.config.type}\n`;
    text += `Period: ${new Date(this.config.dateRange.start).toLocaleDateString()} - ${new Date(this.config.dateRange.end).toLocaleDateString()}\n`;
    text += `\n${'='.repeat(60)}\n\n`;

    for (const section of this.sections) {
      text += `${section.title}\n`;
      text += `${'-'.repeat(section.title.length)}\n`;
      text += `${section.content}\n\n`;
    }

    return text;
  }
}

/**
 * Report generator
 */
class ReportGenerator extends EventEmitter {
  constructor(options = {}) {
    super();

    this.reports = new Map(); // reportId -> Report
    this.dataManager = options.dataManager; // Instance with access to changes, alerts
    this.scheduledReports = new Map(); // scheduleId -> { config, schedule }
    this.options = {
      maxReportSize: options.maxReportSize || 10 * 1024 * 1024, // 10MB
      enablePDFExport: options.enablePDFExport || false
    };
  }

  /**
   * Generate competitor activity report
   */
  generateCompetitorActivityReport(config) {
    const report = new Report(config);
    report.status = 'generating';

    try {
      // Executive summary
      const changes = this.dataManager?.changes || [];
      const monitorChanges = changes.filter(c =>
        !config.monitors.length || config.monitors.includes(c.monitorId)
      );

      const summaryHtml = `
<div>
  <div class="metric">
    <div class="metric-value">${monitorChanges.length}</div>
    <div class="metric-label">Total Changes</div>
  </div>
  <div class="metric">
    <div class="metric-value">${config.monitors.length}</div>
    <div class="metric-label">Monitored Sites</div>
  </div>
</div>
`;

      report.addSection('Executive Summary', summaryHtml, { type: 'metrics' });

      // Change breakdown by type
      const byType = {};
      for (const change of monitorChanges) {
        byType[change.changeType] = (byType[change.changeType] || 0) + 1;
      }

      let typeTable = '<table><thead><tr><th>Change Type</th><th>Count</th><th>%</th></tr></thead><tbody>';
      for (const [type, count] of Object.entries(byType)) {
        const pct = ((count / monitorChanges.length) * 100).toFixed(1);
        typeTable += `<tr><td>${type}</td><td>${count}</td><td>${pct}%</td></tr>`;
      }
      typeTable += '</tbody></table>';

      report.addSection('Changes by Type', typeTable, { type: 'table' });

      // Timeline data
      const timeline = {};
      for (const change of monitorChanges) {
        const date = new Date(change.timestamp).toLocaleDateString();
        timeline[date] = (timeline[date] || 0) + 1;
      }

      let timelineTable = '<table><thead><tr><th>Date</th><th>Changes</th></tr></thead><tbody>';
      for (const [date, count] of Object.entries(timeline).sort()) {
        timelineTable += `<tr><td>${date}</td><td>${count}</td></tr>`;
      }
      timelineTable += '</tbody></table>';

      report.addSection('Activity Timeline', timelineTable, { type: 'table' });

      report.status = 'completed';
      report.content = report.generateHTML();
      report.size = report.content.length;

      this.reports.set(report.id, report);

      this.emit('report:generated', {
        reportId: report.id,
        type: config.type,
        size: report.size
      });

      return report;
    } catch (err) {
      report.status = 'failed';
      report.error = err.message;
      this.emit('report:error', { reportId: report.id, error: err.message });
      throw err;
    }
  }

  /**
   * Generate price history report
   */
  generatePriceHistoryReport(config) {
    const report = new Report(config);
    report.status = 'generating';

    try {
      const changes = this.dataManager?.changes || [];
      const priceChanges = changes.filter(c =>
        c.changeType === 'price_change' &&
        (!config.monitors.length || config.monitors.includes(c.monitorId))
      );

      // Summary metrics
      const summaryHtml = `
<div>
  <div class="metric">
    <div class="metric-value">${priceChanges.length}</div>
    <div class="metric-label">Price Changes Detected</div>
  </div>
</div>
`;

      report.addSection('Summary', summaryHtml);

      // Price movements table
      let movementsTable = '<table><thead><tr><th>Date</th><th>Monitor</th><th>Old Price</th><th>New Price</th><th>Change %</th></tr></thead><tbody>';
      for (const change of priceChanges.slice(0, 50)) {
        const oldPrice = change.oldValue;
        const newPrice = change.newValue;
        const pctChange = oldPrice ? (((newPrice - oldPrice) / oldPrice) * 100).toFixed(2) : 'N/A';

        movementsTable += `<tr><td>${new Date(change.timestamp).toLocaleDateString()}</td><td>${change.monitorId}</td><td>$${oldPrice}</td><td>$${newPrice}</td><td>${pctChange}%</td></tr>`;
      }
      movementsTable += '</tbody></table>';

      report.addSection('Price Movements', movementsTable, { type: 'table' });

      report.status = 'completed';
      report.content = report.generateHTML();
      report.size = report.content.length;

      this.reports.set(report.id, report);

      return report;
    } catch (err) {
      report.status = 'failed';
      report.error = err.message;
      throw err;
    }
  }

  /**
   * Generate technology evolution report
   */
  generateTechEvolutionReport(config) {
    const report = new Report(config);
    report.status = 'generating';

    try {
      const changes = this.dataManager?.changes || [];
      const techChanges = changes.filter(c =>
        c.changeType === 'technology_change' &&
        (!config.monitors.length || config.monitors.includes(c.monitorId))
      );

      // Summary
      const summaryHtml = `
<div>
  <div class="metric">
    <div class="metric-value">${techChanges.length}</div>
    <div class="metric-label">Technology Changes</div>
  </div>
</div>
`;

      report.addSection('Summary', summaryHtml);

      // Technology changes table
      let techTable = '<table><thead><tr><th>Date</th><th>Monitor</th><th>Technology</th><th>Change</th></tr></thead><tbody>';
      for (const change of techChanges.slice(0, 50)) {
        const tech = change.metadata?.technology || 'Unknown';
        const changeDesc = `${change.oldValue} → ${change.newValue}`;

        techTable += `<tr><td>${new Date(change.timestamp).toLocaleDateString()}</td><td>${change.monitorId}</td><td>${tech}</td><td>${changeDesc}</td></tr>`;
      }
      techTable += '</tbody></table>';

      report.addSection('Technology Changes', techTable, { type: 'table' });

      report.status = 'completed';
      report.content = report.generateHTML();
      report.size = report.content.length;

      this.reports.set(report.id, report);

      return report;
    } catch (err) {
      report.status = 'failed';
      report.error = err.message;
      throw err;
    }
  }

  /**
   * Generate alert summary report
   */
  generateAlertSummaryReport(config) {
    const report = new Report(config);
    report.status = 'generating';

    try {
      const alerts = this.dataManager?.alerts || [];
      const filteredAlerts = alerts.filter(a =>
        a.timestamp >= config.dateRange.start &&
        a.timestamp <= config.dateRange.end &&
        (!config.monitors.length || config.monitors.includes(a.monitorId))
      );

      // Summary metrics
      const bySeverity = {};
      const byStatus = {};

      for (const alert of filteredAlerts) {
        bySeverity[alert.severity] = (bySeverity[alert.severity] || 0) + 1;
        byStatus[alert.status] = (byStatus[alert.status] || 0) + 1;
      }

      let summaryHtml = '<div>';
      for (const [severity, count] of Object.entries(bySeverity)) {
        summaryHtml += `<div class="metric"><div class="metric-value">${count}</div><div class="metric-label">${severity.toUpperCase()}</div></div>`;
      }
      summaryHtml += '</div>';

      report.addSection('Alerts by Severity', summaryHtml);

      // Detailed alerts table
      let alertsTable = '<table><thead><tr><th>Date</th><th>Type</th><th>Severity</th><th>Status</th><th>Title</th></tr></thead><tbody>';
      for (const alert of filteredAlerts.slice(0, 100)) {
        alertsTable += `<tr><td>${new Date(alert.timestamp).toLocaleDateString()}</td><td>${alert.type}</td><td>${alert.severity}</td><td>${alert.status}</td><td>${alert.title}</td></tr>`;
      }
      alertsTable += '</tbody></table>';

      report.addSection('Alerts', alertsTable, { type: 'table' });

      report.status = 'completed';
      report.content = report.generateHTML();
      report.size = report.content.length;

      this.reports.set(report.id, report);

      return report;
    } catch (err) {
      report.status = 'failed';
      report.error = err.message;
      throw err;
    }
  }

  /**
   * Get report
   */
  getReport(reportId) {
    return this.reports.get(reportId) || null;
  }

  /**
   * List reports
   */
  listReports(filters = {}) {
    const reports = Array.from(this.reports.values());

    return reports
      .filter(r => {
        if (filters.type && r.config.type !== filters.type) {
          return false;
        }
        if (filters.status && r.status !== filters.status) {
          return false;
        }
        return true;
      })
      .map(r => ({
        id: r.id,
        name: r.config.name,
        type: r.config.type,
        status: r.status,
        generatedAt: r.generatedAt,
        size: r.size
      }));
  }

  /**
   * Schedule report generation
   */
  scheduleReport(config, schedule) {
    const scheduleId = crypto.randomBytes(12).toString('hex');

    this.scheduledReports.set(scheduleId, {
      id: scheduleId,
      config,
      schedule, // { frequency: 'daily|weekly|monthly', time: '09:00' }
      createdAt: Date.now(),
      lastGenerated: null,
      nextGeneration: null
    });

    this.emit('report:scheduled', {
      scheduleId,
      config: config.name,
      schedule: schedule.frequency
    });

    return scheduleId;
  }

  /**
   * Get report content
   */
  getReportContent(reportId, format = 'html') {
    const report = this.reports.get(reportId);
    if (!report) {
      return null;
    }

    if (format === 'html') {
      return report.generateHTML();
    } else if (format === 'text') {
      return report.generateText();
    } else if (format === 'json') {
      return {
        id: report.id,
        config: report.config,
        sections: report.sections,
        generatedAt: report.generatedAt
      };
    }

    return null;
  }

  /**
   * Export report as email body
   */
  getEmailBody(reportId) {
    const report = this.reports.get(reportId);
    if (!report) {
      return null;
    }

    return {
      subject: `${report.config.name} - ${new Date(report.generatedAt).toLocaleDateString()}`,
      htmlBody: report.generateHTML(),
      textBody: report.generateText(),
      attachmentType: 'html'
    };
  }
}

module.exports = {
  ReportGenerator,
  Report,
  ReportConfig
};
