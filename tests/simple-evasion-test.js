/**
 * Simple Bot Evasion Test
 */

const WebSocket = require('ws');
const fs = require('fs');
const path = require('path');

const WS_URL = 'ws://localhost:8765';
const RESULTS_DIR = '/home/devel/basset-hound-browser/tests/results';

class SimpleTest {
  constructor() {
    this.ws = null;
    this.msgId = 0;
    this.pending = new Map();
    this.results = { tests: [], summary: { passed: 0, failed: 0 } };
  }

  async connect() {
    return new Promise((resolve, reject) => {
      this.ws = new WebSocket(WS_URL);
      this.ws.on('open', () => {
        console.log('Connected');
        resolve();
      });
      this.ws.on('message', (data) => {
        try {
          const msg = JSON.parse(data.toString());
          if (msg.type !== 'status') {
            const p = this.pending.get(msg.id);
            if (p) {
              this.pending.delete(msg.id);
              p.resolve(msg);
            }
          }
        } catch (e) {}
      });
      this.ws.on('error', reject);
    });
  }

  async send(command, params = {}) {
    const id = ++this.msgId;
    const msg = { id, command, ...params };
    return new Promise((resolve) => {
      this.pending.set(id, { resolve });
      this.ws.send(JSON.stringify(msg));
      setTimeout(() => {
        if (this.pending.has(id)) {
          this.pending.delete(id);
          resolve({ success: false, error: 'Timeout' });
        }
      }, 30000);
    });
  }

  async test(name, script) {
    console.log(`\nTest: ${name}`);
    try {
      const result = await this.send('execute_script', { script });
      console.log(`  Result: ${result.success ? 'OK' : 'FAIL'}`);
      if (!result.success) {
        console.log(`  Error: ${result.error}`);
        this.results.summary.failed++;
      } else {
        console.log(`  Value: ${result.result}`);
        this.results.summary.passed++;
      }
      this.results.tests.push({ name, success: result.success, value: result.result, error: result.error });
    } catch (e) {
      console.log(`  Exception: ${e.message}`);
      this.results.summary.failed++;
      this.results.tests.push({ name, success: false, error: e.message });
    }
  }

  async run() {
    console.log('\n=== Simple Evasion Test ===');

    // First, try to navigate
    console.log('\nInitializing browser...');
    const navResult = await this.send('navigate', { url: 'about:blank' });
    console.log(`Navigate: ${navResult.success ? 'OK' : 'FAIL'}`);
    await this.wait(1000);

    // Now test scripts
    await this.test('navigator.webdriver', 'typeof navigator.webdriver');
    await this.test('userAgent check', 'navigator.userAgent.substring(0, 50)');
    await this.test('plugins', 'navigator.plugins.length');
    await this.test('chrome check', 'typeof chrome');
    await this.test('headless check', 'navigator.userAgent.includes("Headless") ? "YES" : "NO"');

    this.saveResults();
  }

  wait(ms) {
    return new Promise(r => setTimeout(r, ms));
  }

  saveResults() {
    const report = `# Simple Evasion Test Results

Generated: ${new Date().toISOString()}

## Summary
- Passed: ${this.results.summary.passed}
- Failed: ${this.results.summary.failed}

## Details

${this.results.tests.map(t => `
### ${t.name}
- Success: ${t.success ? 'Yes' : 'No'}
- Value: ${t.value || 'N/A'}
- Error: ${t.error || 'None'}
`).join('\n')}

---
Report generated by Simple Evasion Test
`;

    fs.writeFileSync(
      path.join(RESULTS_DIR, 'BOT-EVASION-REAL-WORLD-VALIDATION-2026-05-08.md'),
      report
    );

    fs.writeFileSync(
      path.join(RESULTS_DIR, 'BOT-EVASION-REAL-WORLD-VALIDATION-2026-05-08.json'),
      JSON.stringify(this.results, null, 2)
    );

    console.log('\nReports saved');
  }
}

(async () => {
  const test = new SimpleTest();
  try {
    await test.connect();
    await test.run();
    test.ws.close();
  } catch (e) {
    console.error('Fatal:', e.message);
    process.exit(1);
  }
})();
